export function contador() {
    let amount = 0;

    function incrementar() {
        amount++;
    }

    function decrementar() {
        amount--;
    }

    function resetear() {
        amount = 0;
    }

    function show() {
        return amount;
    }

    return {
        incrementar,
        decrementar,
        resetear,
        show,
    };
}

